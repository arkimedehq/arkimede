/** Maximum number of facts kept per user (the oldest confirmed ones are pruned). */
const MAX_FACTS_PER_USER = 50;
/** Maximum length of a single fact. */
const MAX_FACT_LEN = 300;

/** Fact proposed by extraction (already persisted as `pending`). */
export interface MemoryProposal {
  id:      string;
  content: string;
}

/** A-MEM note metadata, generated by the summarizer (F1). */
export interface NoteMetadata {
  tags: string[];
  keywords: string[];
  context: string | null;
  category: string | null;
}

/** Extraction output: fact + its metadata (single LLM call for the whole batch). */
